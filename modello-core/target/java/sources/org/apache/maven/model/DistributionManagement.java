package org.apache.maven.model;

  //---------------------------------/
 //- Imported classes and packages -/
//---------------------------------/

import java.util.Date;

/**
 * 
 *         This elements describes all that pertains to
 * distribution for a project.
 *       
 * 
 * @version $Revision$ $Date$
 */
public class DistributionManagement implements java.io.Serializable {


      //--------------------------/
     //- Class/Member Variables -/
    //--------------------------/

    /**
     * Field repository
     */
    private Repository repository;

    /**
     * Field site
     */
    private Site site;


      //-----------/
     //- Methods -/
    //-----------/

    /**
     * Get 
     *             Information needed for deploying to remote
     * repository artifacts generated by the project
     */
    public Repository getRepository()
    {
        return this.repository;
    } //-- Repository getRepository() 

    /**
     * Get Information needed for deploying website files of the
     * project.
     */
    public Site getSite()
    {
        return this.site;
    } //-- Site getSite() 

    /**
     * Set 
     *             Information needed for deploying to remote
     * repository artifacts generated by the project
     * 
     * @param repository
     */
    public void setRepository(Repository repository)
    {
        this.repository = repository;
    } //-- void setRepository(Repository) 

    /**
     * Set Information needed for deploying website files of the
     * project.
     * 
     * @param site
     */
    public void setSite(Site site)
    {
        this.site = site;
    } //-- void setSite(Site) 


    private String modelEncoding = "UTF-8";

    public void setModelEncoding( String modelEncoding )
    {
        this.modelEncoding = modelEncoding;
    }

    public String getModelEncoding()
    {
        return modelEncoding;
    }}
